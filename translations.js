const LANG_STORAGE_KEY = "sportmassage-lang";
const DEFAULT_LANG = "nl";

function setLanguage(lang) {
  if (!translations[lang]) lang = DEFAULT_LANG;
  localStorage.setItem(LANG_STORAGE_KEY, lang);
  document.documentElement.lang = lang === "nl" ? "nl" : lang === "en" ? "en" : lang;
  const t = translations[lang];

  document.querySelectorAll("[data-i18n]").forEach((el) => {
    const key = el.getAttribute("data-i18n");
    if (t[key]) el.textContent = t[key];
  });

  document.querySelectorAll("[data-i18n-placeholder]").forEach((el) => {
    const key = el.getAttribute("data-i18n-placeholder");
    if (t[key]) el.placeholder = t[key];
  });

  const metaDesc = document.querySelector('meta[name="description"]');
  if (metaDesc && t.metaDescription) metaDesc.content = t.metaDescription;

  document.querySelectorAll("[data-i18n-html]").forEach((el) => {
    const key = el.getAttribute("data-i18n-html");
    if (t[key]) el.innerHTML = t[key];
  });

  const currentSelect = document.getElementById("lang-select");
  if (currentSelect) currentSelect.value = lang;
}

function initLanguage() {
  const saved = localStorage.getItem(LANG_STORAGE_KEY);
  const lang = saved && translations[saved] ? saved : DEFAULT_LANG;
  setLanguage(lang);
}

document.addEventListener("DOMContentLoaded", () => {
  initLanguage();
  const langSelect = document.getElementById("lang-select");
  if (langSelect) {
    langSelect.addEventListener("change", (e) => setLanguage(e.target.value));
  }
});
